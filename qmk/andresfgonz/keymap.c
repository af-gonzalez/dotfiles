void oled_render_layer_state(void) {
    oled_write_P(PSTR("Layer: "), false);
    switch (layer_state) {
        case Layer_qwerty:
            oled_write_ln_P(PSTR("Default"), false);
            break;
        case Layer_symbols:
            oled_write_ln_P(PSTR("Symbols"), false);
            break;
        case Layer_arrows:
            oled_write_ln_P(PSTR("Arrows"), false);
            break;
    }
}

